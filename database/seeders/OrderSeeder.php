    /**
     * @return Collection<string, Location>
     */
    private static function getLocations(): Collection
    {
        $locations = Location::all();

        abort_if($locations->count() === 0, 404, 'Locations must be seeded prior to Orders');

        return $locations->keyBy('name');
    }

    /**
     * @return Collection<string, DeliveryCategory>
     */
    private static function getDeliveryCategories(): Collection
    {
        $deliveryCategories = DeliveryCategory::all();

        abort_if($deliveryCategories->count() === 0, 404, 'DeliveryCategory must be seeded prior to Orders');

        return $deliveryCategories->keyBy('name');
    }
